-- Esta función calcula el área de un rectángulo dada su base y altura.
areaRec :: Float -> Float -> Float
areaRec base altura = base * altura
-- La fórmula para calcular el área de un rectángulo es base * altura.

-- Ejemplo de uso:
-- Supongamos que queremos calcular el área de un rectángulo con base 5 y altura 10.
-- Llamamos a la función areaRec y le pasamos los valores 5 y 10 como argumentos.
-- La función devuelve el área del rectángulo, que en este caso es 50.
--    areaRec 5 10
